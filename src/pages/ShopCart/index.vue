    <div class="cart-tool">
      <div class="select-all">
        <input class="chooseAll" type="checkbox" :checked="isAllChecked" />
        <span>全选</span>
      </div>
      <div class="option">
        <a href="#none">删除选中的商品</a>
        <a href="#none">移到我的关注</a>
        <a href="#none">清除下柜商品</a>
      </div>
      <div class="money-box">
        <div class="chosed">已选择 <span>{{}}</span>件商品</div>
        <div class="sumprice">
          <em>总价（不含运费） ：</em>
          <i class="summoney">{{ totalPrice }}</i>
        </div>
        <div class="sumbtn">
          <a class="sum-btn" href="###" target="_blank">结算</a>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import { mapGetters } from "vuex";
import throttle from "lodash/throttle";
export default {
  name: "ShopCart",
  data() {
    return {};
  },
  mounted() {
    this.getData();
  },
  computed: {
    ...mapGetters(["cartList"]),
    cartInfoList() {
      return this.cartList.cartInfoList || [];
    },
    totalPrice() {
      let total = 0;
      this.cartInfoList.forEach((e) => {
        total += e.skuPrice * e.skuNum;
      });
      return total;
    },
    isAllChecked() {
      return this.cartInfoList.every((e) => e.isChecked == 1);
    },
  },
  methods: {
    getData() {
      this.$store.dispatch("getCartList");
    },
    removeCart: throttle(async function (id) {
      try {
        await this.$store.dispatch("deleteCartById", id);
        this.getData();
      } catch (error) {
        console.log(error);
      }
    }, 10),
    async handler(cart, type, num) {
      let changeNum = 0;
      switch (type) {
        case 1:
          changeNum = 1;
          break;
        case -1:
          cart.skuNum > 1 ? (changeNum = -1) : (changeNum = 0);
          break;
        case 0:
          if (isNaN(num * 1) || num <= 0) {
            changeNum = 0;
          } else {
            changeNum = parseInt(num) - cart.skuNum;
          }
          break;
      }
      try {
        await this.$store.dispatch("addOrUpdateShopCart", {
          skuId: cart.skuId,
          skuNum: changeNum,
        });
        this.getData();
      } catch (error) {
        console.log(error);
      }
    },
  },
};
</script>

